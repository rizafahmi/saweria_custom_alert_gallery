    <h1>Cara Penggunaan</h1>
    <p>
        Ini adalah galeri untuk menyimpan custom alert yang dapat digunakan untuk kustomisasi alert Saweria. Berhubung belum ada fitur save dan load. Kamu dapat melakukan kustomisasi alert dengan syarat sudah mendapatkan fitur tersebut. Karena fitur ini masih belum sepenuhnya diluncurkan, atau sudah? Silakan cek tautan berikut <a href="https://saweria.co/overlays/custom/alert/settings" target="_blank">https://saweria.co/overlays/custom/alert/settings</a>. Jika kamu mendapatkan halaman error, 404, seperti ilustrasi dibawah artinya kamu belum kebagian fitur keren ini.

        <img src="/404.png" alt="error 404" />
    </p>

    <h3>1. Buka halaman custom alert</h3>
    <p>Silakan menuju ke tautan <a href="https://saweria.co/overlays/custom/alert/settings" target="_blank">https://saweria.co/overlays/custom/alert/settings</a>. Di halaman tersebut terdapat beberapa opsi. <img src="/halaman.png" alt="halaman custom alert" /></p>
    <h3>2. Copy HTML</h3>
    <p>Lakukan copy isi html di web ini lalu paste ke halaman custom saweria yang telah dibuka tadi. Pastikan kamu paste di bagian yang tepat ya, yaitu HTML. Kamu juga dapat melakukan kustomisasi tambahan.</p>

    <h3>3. Copy CSS</h3>
    <p>Kemudian lakukan copy isi css di web ini lalu paste ke halaman custom saweria yang telah dibuka tadi. Pastikan kamu paste di bagian yang tepat ya, yaitu CSS. Kamu juga dapat melakukan kustomisasi tambahan.</p>

    <h3>4. Simpan Perubahan</h3>
    <p>Simpan perubahan dengan menekan tombol "Save".</p>

    <h3>5. Coba Alert</h3>
    <p> Untuk mencoba alert, tekan tombol "Buka di tab baru" dilanjutkan dengan menekan tombol "Munculkan notifikasi". Pastikan hasilnya sudah sesuai.</p>

    <h3>6. Copy dan paste URL ke Browser Source OBS</h3>
    <p>Langkah terakhir, buka OBS atau Software livestreaming yang digunakan, tambahkan browser source dan paste URL yang didapat dari halaman custom alert saweria kesana.</p>

